var scarc
function setup(){
  createCanvas(400,400)
  angleMode(DEGREES)
}

function draw(){
 background(200)
  translate(200,200)
  rotate(-90)
  var hr = hour()
  var mn = minute()
  var sc=second()
 // text(hr +":"+ mn+":"+sc, 200,200)
 
  noFill()
  stroke("red")
  strokeWeight(7)
  scarc= map(sc, 0, 60 , 0, 360)
  arc(0,0,300,300,0,scarc)
  stroke("blue")
  strokeWeight(10)
  mnarc= map(mn,0,60,0,360)
  arc(0,0,200,200,0,mnarc)
  stroke("green")
  strokeWeight(15)
  hrarc=map(hr%12,0,12,0,360)
  arc(0,0,100,100,0,hrarc)
  stroke("black")
  strokeWeight(5)
  push()
  rotate(scarc)
  line(0,0,150,0)
  pop()
  stroke("black")
  strokeWeight(5)
  push()
  rotate(hrarc)
  line(0,0,50,0)
  pop()
  stroke("black")
  strokeWeight(5)
  push()
  rotate(mnarc)
  line(0,0,100,0)
  pop()
}